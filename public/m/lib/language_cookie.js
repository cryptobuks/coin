function setLang(lang) {
    setLocal('language', lang);
    $.ajax({
        url: API + "set/lang",
        type: "POST",
        dataType: "json",
        data: {
            lang: lang
        },
        async: true,
        success: function (res) {
            // window.location.href = 'index.html';
        }
    });
}
